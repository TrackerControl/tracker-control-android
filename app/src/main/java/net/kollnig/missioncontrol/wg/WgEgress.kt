package net.kollnig.missioncontrol.wg

import android.net.VpnService
import android.os.ParcelFileDescriptor
import android.util.Log

/**
 * Owns the wireguard-go tunnel that sits behind NetGuard's IP-layer hijack.
 *
 * Lifecycle (called from ServiceSinkhole.startNative / stopNative):
 *
 *  1. start(): parses the user's WG config, asks the C side to allocate the
 *     egress socketpair (jni_wireguard_start), then hands the read-end fd
 *     plus the VpnService TUN fd to wgbridge.Wgbridge.startTunnel. From this
 *     point on, packets the C side hijacks at ip.c::handle_ip flow through
 *     wireguard-go and out a protect()-ed UDP socket.
 *
 *  2. stop(): tears down the wireguard-go device first (so no further
 *     decrypted packets land on the VpnService TUN), then jni_wireguard_stop
 *     to flip the C-side flag and close the write end.
 *
 * The wgbridge classes referenced here are generated by `gomobile bind` —
 * see ../../../../../../wgbridge/README.md for build instructions.
 */
object WgEgress {
    private const val TAG = "WgEgress"
    private const val DEFAULT_MTU = 1420

    @Volatile private var tunnel: Any? = null   // net.kollnig.missioncontrol.wgbridge.Tunnel

    /**
     * Returns true if the tunnel started successfully. On failure returns false
     * and ensures the C side is back to wg_enabled=0 so traffic doesn't get
     * black-holed.
     */
    fun start(
        configText: String,
        vpnService: VpnService,
        vpnFd: ParcelFileDescriptor,
        startSocketpair: () -> Int,
        stopSocketpair: () -> Unit
    ): Boolean {
        if (tunnel != null) {
            Log.w(TAG, "start() called while already running")
            return true
        }

        val parsed = try {
            WgConfigParser.parse(configText)
        } catch (e: Exception) {
            Log.e(TAG, "config parse: ${e.message}")
            return false
        }

        // Resolve any peer endpoint hostnames BEFORE the C-side hijack flag
        // is flipped — otherwise this DNS query gets captured by NetGuard,
        // handed to wireguard-go (which has no peer yet), and dropped.
        val config = try {
            withResolvedEndpoints(parsed)
        } catch (e: Exception) {
            Log.e(TAG, "endpoint resolve: ${e.message}")
            return false
        }

        val rxFd = startSocketpair()
        if (rxFd < 0) {
            Log.e(TAG, "jni_wireguard_start failed")
            return false
        }

        val tunFd = vpnFd.fd
        val mtu = config.mtu ?: DEFAULT_MTU

        try {
            // Reflective lookup so this file compiles before wgbridge.aar is
            // produced. Once the AAR is in app/libs/, replace this block with
            // a direct call: Wgbridge.startTunnel(...).
            val wgbridgeClass = Class.forName("net.kollnig.missioncontrol.wgbridge.Wgbridge")
            val protectorClass = Class.forName("net.kollnig.missioncontrol.wgbridge.Protector")
            val loggerClass = Class.forName("net.kollnig.missioncontrol.wgbridge.Logger")
            val startTunnel = wgbridgeClass.getMethod(
                "startTunnel",
                String::class.java,
                Int::class.javaPrimitiveType,
                Int::class.javaPrimitiveType,
                Int::class.javaPrimitiveType,
                protectorClass,
                loggerClass
            )
            val protector = java.lang.reflect.Proxy.newProxyInstance(
                protectorClass.classLoader,
                arrayOf(protectorClass)
            ) { _, method, args ->
                if (method.name == "protect" && args != null && args.size == 1) {
                    vpnService.protect(args[0] as Int)
                } else {
                    false
                }
            }
            val logger = java.lang.reflect.Proxy.newProxyInstance(
                loggerClass.classLoader,
                arrayOf(loggerClass)
            ) { _, method, args ->
                val msg = (args?.firstOrNull() as? String).orEmpty()
                when (method.name) {
                    "verbosef" -> Log.v(TAG, msg)
                    "errorf"   -> Log.e(TAG, msg)
                }
                null
            }
            tunnel = startTunnel.invoke(
                null, config.toUapi(), rxFd, tunFd, mtu, protector, logger
            )
        } catch (e: Throwable) {
            Log.e(TAG, "wgbridge.startTunnel failed", e)
            stopSocketpair()
            return false
        }

        return true
    }

    fun stop(stopSocketpair: () -> Unit) {
        val t = tunnel
        tunnel = null
        if (t != null) {
            try {
                t.javaClass.getMethod("stop").invoke(t)
            } catch (e: Throwable) {
                Log.w(TAG, "tunnel.stop threw", e)
            }
        }
        stopSocketpair()
    }

    fun isRunning(): Boolean = tunnel != null

    private fun withResolvedEndpoints(config: WgConfig): WgConfig {
        return config.copy(peers = config.peers.map { peer ->
            val ep = peer.endpoint
            if (ep == null) peer else peer.copy(endpoint = resolveEndpoint(ep))
        })
    }

    /**
     * "host:port" or "[v6-host]:port" -> "ip:port" or "[ip]:port".
     * Hostname is resolved synchronously; caller must run off the main thread.
     */
    private fun resolveEndpoint(endpoint: String): String {
        val (host, port) = if (endpoint.startsWith("[")) {
            val close = endpoint.indexOf(']')
            require(close > 0 && endpoint.length > close + 2 && endpoint[close + 1] == ':') {
                "malformed IPv6 endpoint: $endpoint"
            }
            endpoint.substring(1, close) to endpoint.substring(close + 2)
        } else {
            val colon = endpoint.lastIndexOf(':')
            require(colon > 0 && colon < endpoint.length - 1) {
                "missing :port in endpoint: $endpoint"
            }
            endpoint.substring(0, colon) to endpoint.substring(colon + 1)
        }
        val addr = java.net.InetAddress.getByName(host)
        val ip = addr.hostAddress ?: throw IllegalStateException("getHostAddress null for $host")
        return if (addr is java.net.Inet6Address) "[$ip]:$port" else "$ip:$port"
    }
}
